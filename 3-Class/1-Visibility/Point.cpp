#include "Point.hpp"   					     	   
//Definition des methodes de la classe Point   					     	   
   					     	   
Point::Point(){   					     	   
}   					     	   
   					     	   
void Point::setX(int x){   					     	   
    this->m_x = x;   					     	   
}   					     	   
void Point::setY(int y){   					     	   
    this->m_y = y;   					     	   
}   					     	   
   					     	   
int Point::getX(){   					     	   
    return m_x;   					     	   
}   					     	   
int Point::getY(){   					     	   
    return m_y;   					     	   
}   					     	   